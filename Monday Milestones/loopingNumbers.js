//print every integer
console.log('Printing off every integer between 40 and 60:')
for (let i = 40; i < 61; i++) {

  console.log(i)
}


// print every even integer
console.log('Printing off every even integer between 40 and 60:')
for (let i = 40; i < 61; i += 2) {

  console.log(i)
}


